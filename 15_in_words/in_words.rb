class Fixnum
        
  def in_words
    number = self.to_f.to_i
    case number
      when 0 then "zero"
      when 1 then "one"
      when 2 then "two"
      when 3 then "three"
      when 4 then "four"
      when 5 then "five"
      when 6 then "six"
      when 7 then "seven"
      when 8 then "eight"
      when 9 then "nine"
      when 10 then "ten"
      when 11 then "eleven"
      when 12 then "twelve"
      when 13 then "thirteen"
      when 15 then "fifteen"
      when 18 then "eighteen"
      when 14..19 then "#{(number - 10).in_words + "teen"}"
      when 20 then "twenty"
      when 30 then "thirty"
      when 40 then "forty"
      when 50 then "fifty"
      when 60 then "sixty"
      when 70 then "seventy"
      when 80 then "eighty"
      when 90 then "ninety"
      when 21..99 then "#{((number / 10) * 10 ).in_words}#{ " #{(number % 10).in_words}" unless (number % 10).zero?}"
      when 100 then "one hundred"
      when 200 then "two hundred"
      when 300 then "three hundred"
      when 400 then "four hundred"
      when 500 then "five hundred"
      when 600 then "six hundred"
      when 700 then "seven hundred"
      when 800 then "eight hundred"
      when 900 then "nine hundred"
      when 101..999 then "#{((number / 100) * 100 ).in_words}#{ " #{(number % 100).in_words}" unless (number % 100).zero?}"
      when 1000 then "one thousand"
      when 2000 then "two thousand"
      when 3000 then "three thousand"
      when 4000 then "four thousand"
      when 5000 then "five thousand"
      when 6000 then "six thousand"
      when 7000 then "seven thousand"
      when 8000 then "eight thousand"
      when 9000 then "nine thousand"
      when 1001..9999 then "#{((number / 1000) * 1000 ).in_words}#{ " #{(number % 1000).in_words}" unless (number % 1000).zero?}"
      when 10_000..999_999 then "#{(number / 1000).in_words} thousand#{ " #{(number % 1000).in_words}" unless (number % 1000).zero?}"
      when 1_000_000..999_999_999 then "#{(number / 1_000_000).in_words} million#{ " #{(number % 1_000_000).in_words}" unless (number % 1_000_000).zero?}"
      when 1_000_000_000..999_999_999_999 then "#{(number / 1_000_000_000).in_words} billion#{ " #{(number % 1_000_000_000).in_words}" unless (number % 1_000_000_000).zero?}"
      when 1_000_000_000_000..999_999_999_999_999 then "#{(number / 1_000_000_000_000).in_words} trillion#{ " #{(number % 1_000_000_000_000).in_words}" unless (number % 1_000_000_000_000).zero?}"
    end
  end
end